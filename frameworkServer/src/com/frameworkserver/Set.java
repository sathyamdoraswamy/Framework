package com.frameworkserver;

/**
 * This class stores class object and metadata and is used as a return type in functions
 * @author sathyam
 */
public class Set {

    /**
     * groupid to identify the object
     */
    public String groupid;
    /**
     * namespace to which the object belongs
     */
    public String namespace;
    /**
     * class object
     */
    public Object obj;
}
